#include<unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int		i;

	i = 0;
	while (s[i])
	{
		i++;
	}
	char	buf[i];
	
	buf[i] = '\0';
	write(fd, buf, i);
}
